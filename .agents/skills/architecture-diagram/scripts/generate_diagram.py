def generate_container_diagram(result: CrawlResult, output_dir: Path) -> Path:  # noqa: PLR0912
    """Generate the L2 Container diagram showing individual projects.

    Args:
        result: Crawl result data.
        output_dir: Directory to write the diagram file.

    Returns:
        Path to the generated diagram file.

    """
    out = output_dir / "l2_container.py"

    lines = [
        '"""L2 Container — Ansible Dev Tools Ecosystem."""',
        "from c4 import *",
        "",
        "",
        f'with ContainerDiagram("{PROVENANCE}") as diagram:',
        "",
        "    # --- Personas ---",
        "    content_author = Person(",
        '        "Ansible Content Author",',
        '        "Writes playbooks, roles, and collections.",',
        "    )",
        "",
    ]

    # Group by tier into boundaries
    tier_groups = {
        RepoTier.PRIMARY: ("ADT Core Tools", "adt_core", []),
        RepoTier.EXPERIMENTAL: ("ADT Testing and Automation", "adt_testing", []),
        RepoTier.COMMUNITY: ("Community", "community", []),
        RepoTier.CONTAINER: ("Downstream Containers", "downstream", []),
        RepoTier.AI_PLATFORM: ("Abbenay - AI Daemon", "abbenay_ai", []),
        RepoTier.ADJACENT: ("APME - Adjacent", "apme_adjacent", []),
    }
    for slug, entry in result.repos.items():
        if entry.tier in tier_groups:
            tier_groups[entry.tier][2].append((slug, entry))

    var_map: dict[str, str] = {}

    for tier, (boundary_label, boundary_alias, entries) in tier_groups.items():
        if not entries:
            continue

        lines.append(f"    # --- {boundary_label} ---")
        lines.append(
            f'    with ContainerBoundary("{boundary_label}", alias="{boundary_alias}"):',
        )

        for slug, entry in entries:
            var = _safe_var(slug)
            var_map[slug] = var
            tech = entry.language.value
            short_name = slug.rsplit("/", maxsplit=1)[-1]
            desc = entry.description.replace('"', '\\"')

            if tier == RepoTier.CONTAINER:
                lines.append(
                    f'        {var} = Container("{short_name}", "{desc}", technology="Container Image", alias="{var}")',
                )
            else:
                lines.append(
                    f'        {var} = Container("{short_name}", "{desc}", technology="{tech}", alias="{var}")',
                )
        lines.append("")

    # Add components discovered inside repos (vscode lang server, MCP, abbenay sub-packages)
    for comp in result.components:
        var = _safe_var(comp.name)
        if var in var_map.values():
            var = var + "_comp"
        var_map[comp.name] = var
        desc = comp.description.replace('"', '\\"')
        tech = comp.technology.replace('"', '\\"')
        lines.append(
            f'    {var} = Container("{comp.name}", "{desc}", technology="{tech}", alias="{var}")',
        )

    lines.append("")

    # Container images built by repos (only show images with registry names)
    image_rels: list[str] = []
    seen_image_vars: set[str] = set()
    for container in result.containers:
        if "/" not in container.image_name:
            continue
        var = _safe_var(container.image_name)
        if var in seen_image_vars:
            continue
        seen_image_vars.add(var)
        short = container.image_name.split("/")[-1]
        var_map[container.image_name] = var
        lines.append(
            f'    {var} = Container("{short}", '
            f'"Published container image", technology="Container Image", alias="{var}")',
        )
        repo_var = var_map.get(container.repo_slug)
        if repo_var:
            image_rels.append(f'    {repo_var} >> Rel("builds") >> {var}')

    if seen_image_vars:
        lines.append("")

    # External systems
    lines += [
        "    # --- External Dependencies ---",
        "    aap_portal = ContainerExt("
        '"AAP Portal", "Custom Backstage instance for AAP self-service", '
        'technology="Backstage", alias="aap_portal"'
        ")",
        "    ansible_builder = ContainerExt("
        '"ansible-builder", "Execution environment builder", '
        'technology="Python", alias="ansible_builder"'
        ")",
        "    ansible_runner = ContainerExt("
        '"ansible-runner", "Playbook execution engine", '
        'technology="Python", alias="ansible_runner"'
        ")",
        "    ansible_core = ContainerExt("
        '"ansible-core", "Core Ansible engine", '
        'technology="Python", alias="ansible_core"'
        ")",
        "    community_molecule = ContainerExt("
        '"community.molecule", "Molecule collection", '
        'technology="Ansible Collection", alias="community_molecule"'
        ")",
        "    vscode_yaml = ContainerExt("
        '"vscode-yaml", "YAML language support", '
        'technology="TypeScript", alias="vscode_yaml"'
        ")",
        "    llm_apis = ContainerExt("
        '"LLM Provider APIs", "OpenAI, Anthropic, Gemini, Ollama", '
        'technology="HTTP APIs", alias="llm_apis"'
        ")",
        "",
    ]

    ext_vars = {
        "ansible/ansible-builder": "ansible_builder",
        "ansible/ansible-runner": "ansible_runner",
        "ansible/ansible-core": "ansible_core",
        "ansible-collections/community.molecule": "community_molecule",
        "redhat-developer/vscode-yaml": "vscode_yaml",
    }

    # Relationships from crawl data
    lines.append("    # --- Relationships (discovered from actual repos) ---")
    lines.append(
        '    content_author >> Rel("Develops with") >> ' + var_map.get("ansible/ansible-dev-tools", "adt"),
    )
    lines.append("")

    for rel in result.relationships:
        src_var = var_map.get(rel.source) or ext_vars.get(rel.source)
        tgt_var = var_map.get(rel.target) or ext_vars.get(rel.target)
        if not src_var or not tgt_var:
            continue
        if src_var == tgt_var:
            continue
        lines.append(f'    {src_var} >> Rel("{rel.label}") >> {tgt_var}')

    # Container image build relationships
    for img_rel in image_rels:
        lines.append(img_rel)

    # AAP Portal consumes the downstream container and ADT REST API
    lines.append("")
    lines.append("    # --- AAP Portal (external consumer) ---")
    devtools_container_var = var_map.get(
        "ansible-automation-platform/ansible-devtools-container",
    )
    adt_var = var_map.get("ansible/ansible-dev-tools")
    if devtools_container_var:
        lines.append(
            f'    aap_portal >> Rel("Consumes ADT web server") >> {devtools_container_var}',
        )
    if adt_var:
        lines.append(f'    aap_portal >> Rel("Consumes REST API") >> {adt_var}')

    code = "\n".join(lines) + "\n"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(code)
    return out


# ---------------------------------------------------------------------------
# L3 -- Component Diagram (per-repo, on request)
# ---------------------------------------------------------------------------


def generate_component_diagram(
    result: CrawlResult,
    repo_slug: str,
    output_dir: Path,
) -> Path | None:
    """Generate an L3 Component diagram for a single repo.

    Args:
        result: Crawl result data.
        repo_slug: Repository slug to generate diagram for.
        output_dir: Directory to write the diagram file.

    Returns:
        Path to the generated diagram file, or ``None`` on failure.

    """
    entry = result.repos.get(repo_slug)
    if not entry:
        print(f"  Unknown repo: {repo_slug}", file=sys.stderr)
        return None

    components = [c for c in result.components if c.repo_slug == repo_slug]
    if not components:
        print(f"  No components discovered for {repo_slug}", file=sys.stderr)
        return None

    safe_slug = _safe_var(repo_slug)
    out = output_dir / f"l3_component_{safe_slug}.py"

    lines = [
        f'"""L3 Component — {repo_slug}."""',
        "from c4 import *",
        "",
        "",
        f'with ComponentDiagram("{repo_slug} Components") as diagram:',
        "",
        f'    with ContainerBoundary("{repo_slug}", alias="{safe_slug}"):',
    ]

    var_map: dict[str, str] = {}
    for comp in components:
        var = _safe_var(comp.name)
        var_map[comp.name] = var
        desc = comp.description.replace('"', '\\"')
        tech = comp.technology.replace('"', '\\"')
        lines.append(
            f'        {var} = Component("{comp.name}", "{desc}", technology="{tech}", alias="{var}")',
        )

    lines.append("")

    # Collect relationships involving this repo's components
    repo_rels = [r for r in result.relationships if r.source in var_map or r.target in var_map]

    # Identify external targets/sources that need ComponentExt definitions
    ext_vars: dict[str, str] = {}
    for rel in repo_rels:
        for endpoint in (rel.source, rel.target):
            if endpoint not in var_map and endpoint not in ext_vars:
                var = _safe_var(endpoint) + "_ext"
                ext_vars[endpoint] = var
                short_name = endpoint.split("/")[-1] if "/" in endpoint else endpoint
                ext_desc = result.repos[endpoint].description.replace('"', '\\"') if endpoint in result.repos else ""
                lines.append(
                    f'    {var} = ComponentExt("{short_name}", "{ext_desc}", alias="{var}")',
                )

    if ext_vars:
        lines.append("")

    for rel in repo_rels:
        src_var = var_map.get(rel.source) or ext_vars.get(rel.source)
        tgt_var = var_map.get(rel.target) or ext_vars.get(rel.target)
        if src_var and tgt_var and src_var != tgt_var:
            lines.append(f'    {src_var} >> Rel("{rel.label}") >> {tgt_var}')

    code = "\n".join(lines) + "\n"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(code)
    return out


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> None:
    """Generate C4 diagram files from crawled dependency data."""
    parser = argparse.ArgumentParser(
        description="Generate C4 diagram files from crawl data",
    )
    parser.add_argument(
        "--input",
        default=".architecture-diagrams/dependencies.json",
        help="Crawl result JSON",
    )
    parser.add_argument(
        "--output-dir",
        default=".architecture-diagrams",
        help="Output directory for diagram .py files",
    )
    parser.add_argument(
        "--level",
        choices=["context", "container", "component", "all"],
        default="all",
        help="Which C4 level(s) to generate",
    )
    parser.add_argument(
        "--component-repo",
        default="",
        help="Repo slug for L3 component diagram (requires --level component)",
    )
    args = parser.parse_args()

    input_path = Path(args.input)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"Loading crawl data from {input_path} ...")
    result = CrawlResult.from_json(input_path)
    print(
        f"  {len(result.repos)} repos, {len(result.relationships)} relationships, {len(result.components)} components",
    )

    generated: list[Path] = []

    if args.level in ("context", "all"):
        p = generate_system_context(result, output_dir)
        generated.append(p)
        print(f"  Generated L1 System Context: {p}")

    if args.level in ("container", "all"):
        p = generate_container_diagram(result, output_dir)
        generated.append(p)
        print(f"  Generated L2 Container: {p}")

    if args.level == "component":
        if not args.component_repo:
            # Generate for all repos that have components
            repos_with_components = {c.repo_slug for c in result.components}
            for slug in sorted(repos_with_components):
                p = generate_component_diagram(result, slug, output_dir)
                if p:
                    generated.append(p)
                    print(f"  Generated L3 Component ({slug}): {p}")
        else:
            p = generate_component_diagram(result, args.component_repo, output_dir)
            if p:
                generated.append(p)
                print(f"  Generated L3 Component ({args.component_repo}): {p}")

    if args.level == "all":
        repos_with_components = {c.repo_slug for c in result.components}
        for slug in sorted(repos_with_components):
            p = generate_component_diagram(result, slug, output_dir)
            if p:
                generated.append(p)
                print(f"  Generated L3 Component ({slug}): {p}")

    print(f"\n{len(generated)} diagram file(s) generated in {output_dir}/")


if __name__ == "__main__":
    main()
