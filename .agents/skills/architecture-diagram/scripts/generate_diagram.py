"""Generate C4 diagram Python files from crawled dependency data.

Usage:
    python3 generate_diagram.py [--input dependencies.json] \
        [--output-dir .architecture-diagrams] [--level context|container|component]

Reads the crawler's dependencies.json and produces executable Python files
that use the c4-diagrams DSL. Each file can be rendered to PNG via `c4 export`.
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

try:
    from arch_models import (  # pylint: disable=import-error
        CrawlResult,
        RepoTier,
    )
except ImportError:
    sys.path.insert(0, str(Path(__file__).resolve().parent))
    from arch_models import (
        CrawlResult,
        RepoTier,
    )

PROVENANCE = (
    "Generated by architecture-diagram skill — "
    "https://github.com/ansible/team-devtools/tree/main/.agents/skills/architecture-diagram"
)


def _safe_var(name: str) -> str:
    """Turn a repo slug or component name into a valid Python variable.

    Args:
        name: Repo slug or component name.

    Returns:
        Valid Python identifier.

    """
    result = name.replace("/", "_").replace("-", "_").replace(".", "_").replace("@", "").replace(" ", "_").lower()
    result = re.sub(r"[^a-z0-9_]", "", result)
    result = re.sub(r"_+", "_", result).strip("_")
    if result and result[0].isdigit():
        result = "n" + result
    return result or "unnamed"


# ---------------------------------------------------------------------------
# L1 -- System Context Diagram
# ---------------------------------------------------------------------------


def generate_system_context(result: CrawlResult, output_dir: Path) -> Path:
    """Generate the L1 System Context diagram.

    Args:
        result: Crawl result data.
        output_dir: Directory to write the diagram file.

    Returns:
        Path to the generated diagram file.

    """
    out = output_dir / "l1_system_context.py"

    # Group repos into logical systems
    adt_repos = [
        s for s, e in result.repos.items() if e.tier in (RepoTier.PRIMARY, RepoTier.EXPERIMENTAL, RepoTier.COMMUNITY)
    ]
    container_repos = [s for s, e in result.repos.items() if e.tier == RepoTier.CONTAINER]
    ai_platform_repos = [s for s, e in result.repos.items() if e.tier == RepoTier.AI_PLATFORM]
    adjacent_repos = [s for s, e in result.repos.items() if e.tier == RepoTier.ADJACENT]

    lines = [
        '"""L1 System Context — Ansible Dev Tools Ecosystem."""',
        "from c4 import *",
        "",
        "",
        f'with SystemContextDiagram("{PROVENANCE}") as diagram:',
        "",
        "    # --- Personas ---",
        "    content_author = Person(",
        '        "Ansible Content Author",',
        '        "Writes playbooks, roles, and collections using Ansible.",',
        "    )",
        "",
        "    # --- ADT System ---",
        '    with EnterpriseBoundary("Ansible DevTools"):',
        "        adt = System(",
        '            "Ansible Dev Tools",',
        '            "Suite of CLI tools, libraries, and extensions for Ansible content development. '
        f'Includes {len(adt_repos)} projects.",',
        "        )",
        "",
        "        vscode_ext = System(",
        '            "VS Code Ansible Extension",',
        '            "IDE extension with language server, MCP server, and Python tool integrations.",',
        "        )",
        "",
    ]

    if container_repos:
        lines += [
            "    # --- Downstream Containers ---",
            '    with SystemBoundary("AAP Downstream Containers"):',
        ]
        for slug in container_repos:
            entry = result.repos[slug]
            var = _safe_var(slug)
            short = slug.split("/")[-1]
            desc = entry.description.replace('"', '\\"')
            lines.append(f'        {var} = System("{short}", "{desc}", alias="{var}")')
        lines.append("")

    if ai_platform_repos:
        lines += [
            "    # --- AI Platform ---",
        ]
        for slug in ai_platform_repos:
            entry = result.repos[slug]
            var = _safe_var(slug)
            short = slug.split("/")[-1].title()
            desc = entry.description.replace('"', '\\"')
            lines.append(f'    {var} = System("{short}", "{desc}", alias="{var}")')
        lines.append("")

    if adjacent_repos:
        lines += [
            "    # --- Adjacent Systems ---",
        ]
        for slug in adjacent_repos:
            entry = result.repos[slug]
            var = _safe_var(slug)
            short = slug.split("/")[-1].upper()
            desc = entry.description.replace('"', '\\"')
            lines.append(f'    {var} = SystemExt("{short}", "{desc}", alias="{var}")')
        lines.append("")

    lines += [
        "    # --- External Systems ---",
        "    aap_portal = SystemExt("
        '"AAP Portal", "Custom Backstage instance for AAP self-service", alias="aap_portal"'
        ")",
        '    pypi = SystemExt("PyPI", "Python package index", alias="pypi")',
        '    github = SystemExt("GitHub", "Source code hosting and CI/CD", alias="github")',
        '    galaxy = SystemExt("Ansible Galaxy", "Collection distribution", alias="galaxy")',
        '    llm_apis = SystemExt("LLM Provider APIs", "OpenAI, Anthropic, Gemini, Ollama", alias="llm_apis")',
        "",
        "    # --- Relationships ---",
        '    content_author >> Rel("Develops content with") >> adt',
        '    content_author >> Rel("Edits in IDE") >> vscode_ext',
        '    vscode_ext >> Rel("Spawns CLI tools") >> adt',
        '    adt >> Rel("Publishes packages") >> pypi',
        '    adt >> Rel("Hosts code") >> github',
        '    adt >> Rel("Publishes collections") >> galaxy',
    ]

    if container_repos:
        lines.append(
            f'    adt >> Rel("Packaged into") >> {_safe_var(container_repos[0])}',
        )
        lines.append(
            f'    aap_portal >> Rel("Consumes ADT web server") >> {_safe_var(container_repos[0])}',
        )

    lines.append('    aap_portal >> Rel("Consumes REST API") >> adt')

    if ai_platform_repos:
        ai_var = _safe_var(ai_platform_repos[0])
        lines.append(f'    {ai_var} >> Rel("Provides LLM access") >> llm_apis')

    if adjacent_repos:
        apme_var = _safe_var(adjacent_repos[0])
        if ai_platform_repos:
            lines.append(
                f'    {apme_var} >> Rel("AI remediation via") >> {_safe_var(ai_platform_repos[0])}',
            )

    code = "\n".join(lines) + "\n"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(code)
    return out


